const a = 7; //Atribuição
let b = 3;

b+=a;  //Isto e igual a (b = b + a);
console.log(b);

b-=4   //Isto e igual a (b = b - 4), lembrando que o valor de B aqui e de 10 pela operação acima, que vai acontecer sempre q alterarmos seu valor; 
console.log(b);

b*=2;  //Isto e igual a (b = b * 2);
console.log(b);

b/=2;  //Isto e igual a (b = b / 2);
console.log(b);

b%=2;  //Isto e igual a (b = b % 2); resto da divisao sempre sera, 1-(significa N° Impar) ou 0-(significa N° Par);
console.log(b);

